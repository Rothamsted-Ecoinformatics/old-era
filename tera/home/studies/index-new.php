<h1>Case Studies</h1>

<p>Data held in e-RA provide a unique historic record from experiments that have measured the effects of 
agricultural practices on soils, crops and associated ecosystems continuously for over 160 years. Together
 with the retrospective analysis of archived material from the Rothamsted Sample Archive, these data
 continue to reveal new insights and important findings of relevance to today's agriculture and the wider
 environment. 

 </p>
<p><strong>Agronomy - fertiliser recomendations</strong></p>
<table width="631" border="2">
  <tr>
    <td width="262" height="200">&nbsp;</td>
    <td width="351"><strong>Nitrogen cycling:</strong> The  long-term experiments have been an ideal test-bed for detailed studies of the  nitrogen cycle in crop and soil, using the stable isotope 15N. This  has increased our understanding of the fate of nitrogen fertilizer, and enabled  the development of practices to reduce pollution and use scarce resources more  efficiently. Results are part of the knowledge base for DEFRA&rsquo;s Fertilizer  Recommendation System, RB209.    </td>
  </tr>
  <tr>
    <td>&nbsp;</td>
    <td><strong>Phosporous  fertilizer recommendations:</strong> The LTEs are essential for studying the supply of  the plant nutrient phosphorus (P) to crops. Rates of change of P reserves have  been studied in the Exhaustion Land experiment, where no P had been applied for  80 years. P was then applied at various rates to measure the rate of increase  of crop-available P. It was discovered that crop yields increase with available  soil P, up to a critical value, beyond this value, there is no further increase  in yield. This approach is the basis of P fertilizer advice in the UK and  several other countries (Poulton et al, 2013).
