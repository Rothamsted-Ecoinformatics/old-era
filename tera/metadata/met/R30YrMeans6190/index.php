<h2>Provenance &amp; Quality</h2>
<p>This 30-year mean meteorological data contained in this spreadsheet are derived from  daily data measured at Rothamsted Research, Harpenden, Herts, UK.
Mean monthly data is determined from daily data using  Genstat 19th Edition.   Missing values are acquired from the Met Office Station on site. Verification of daily data includes checks for instrument errors, for missing data and outliers. The original raw daily data is available, after registering, from the e-RA database. Please contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a> for an access password and further details.</p>
