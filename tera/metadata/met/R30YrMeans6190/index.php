<h2>Description</h2>
<p>This dataset consists of  the 30-year monthly mean meteorological data measured at Rothamsted Research, Harpenden, UK, from January 1961 until December 1990,
and is derived from daily measurements taken at the site. Thirty years is chosen as a period long enough to eliminate year-to-year variations. This dataset precedes the <a href="Met/R30YrMeans7100" target="_blank">1971-2000 dataset </a>and the <a href="Met/R30YrMeans8110" target="_blank">1981-2010 dataset.</a></p>
<p><strong>MEASUREMENTS</strong></p>
<li><b>Rain: </b>The mean total monthly rainfall including all precipitation (snow, rain, mist and fog) captured in a 5 inch rain gauge (mm per month); <br />
<li><b>RainL: </b>The mean total monthly rainfall including all precipitation (snow, rain, mist and fog) captured in a 1/1000 acre rain gauge (mm per month); <br />
</li>
<li><strong>Sunshine: </strong>The mean total  hours of sunshine (hours per month)<br />
</li>
<li><b>Maximum temperature: </b>The mean maximum monthly air temperature (degrees C);</li>
<li><b>Minimum temperature: </b>The mean minumum monthly air temperature (degrees C);</li>
<li><b>Average temperature: T</b>he mean of the maximum and minimum air temperature (degrees C);</li>
<li><b>Soil temperature under grass 30cm: </b>The mean monthly soil temperature  under grass at 30cm depth, measured at 0900 GMT (degrees C); </li>
<li><b>Soil temperature under grass 100cm: </b>The mean monthly soil temperature under grass at 100cm depth, measured at 0900 GMT (degrees C); </li>
<li><b>Wind: </b>The mean monthly wind speed measured at 2 metres above ground level, averaged over 24 hours (km per hour); </li>
<li><b>Evaporation: </b>The mean total monthly evaporation over two open water tanks installed in the spring of each year (mm per month). </li>
<li><b>Radiation: </b>The mean daily solar radiation measured with a Kipp Solarimeter (J/cm2); </li>
