<div align="justify"><strong>Mean long-term spring barley yields</strong> from selected treatments on the Hoosfield experiment are shown. Spring barley has been grown continuously since 1852. The changes in yield reflect the improved varieties, cultivations and control of weeds and diseases that have been introduced, especially since the late 1960s.
