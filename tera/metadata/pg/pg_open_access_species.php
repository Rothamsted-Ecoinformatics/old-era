<h2>Description</h2>
<p>The Figure shows the impact of selected treatments on the  number of species comprising 1% or more of the biomass. Applying N as sodium  nitrate (orange line N*) or ammonium sulphate (blue line N) quickly decreased  species numbers relative to the unfertilised control.&nbsp; The continued  application of ammonium decreases species numbers further because of its  acidifying effect on the soil (pH 3.6 in 2011, solid blue line), reducing the number  of species to one or two, <em>Holcus lanatus</em> (Yorkshire Fog) and <em>Anthoxanthum  odoratum</em> (Sweet Vernal Grass). However, even on the nil plot (solid green  line) the number of species has decreased. This may be due to changes in  atmospheric inputs and/or changes in sward management.</p>
<p>Applying chalk (dotted lines) increased soil pH (pH 6.2 in  2011, dotted blue line) and helped some of the species return  to the  original numbers. 
  Recent work (Storkey et al 2015) showed that on some plots  withholding N helped increase species diversity as did decreased atmospheric N  deposition, as evident in the species numbers of the nil plot (solid green  line). </p>
<p><strong>AGRONOMIC PRACTICES</strong></p>
<p>Fertiliser treatments since 1856;<br />
  <strong>No fertiliser - Plot 3</strong>: No fertilizer or manure<br />
  <strong>N*2PKNaMg - Plot 14:</strong> 96kgN as sodium nitrate plus P,K,Na and Mg<br />
<strong>N2PKNaMg - Plot 9:</strong> 96kgN as ammonium sulphate plus P, K, Na and Mg</p>
<p><strong>Chalk:</strong> Ground chalk (CaCO3) applied approximately every four years 1903 - 1964 then as required to maintain pH at around 6.</p>
