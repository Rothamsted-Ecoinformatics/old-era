<h1>Description</h1>
<p>This dataset consists of annual wheat yields from selected plots of the Broadbalk Wheat Experiment, 1852-1918, as used by R.A. Fisher in his 1921 paper 'Studies in crop variation'. It is the
annual data used to derive the mean values published in Table 1 of this paper. </p>
<p><h1>Data</h1></p>
<p>Data are presented as annual dressed wheat grain yields, in bushels/acre, from the Broadbalk Wheat Experiment, Rothamsted Research, Harpenden, Herts, AL5 2JQ, UK. 
Data is given for 13 plots (2b, 3&4, 5, 6, 7, 8, 10, 11, 12, 13, 14, 17 and 18), from 1852-1918 inclusive.
 </p>

