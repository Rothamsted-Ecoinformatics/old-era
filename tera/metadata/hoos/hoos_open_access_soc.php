<h2>Description</h2>
<p>The  figure shows changes in soil organic carbon (SOC) content (tha<sup>-1</sup>) in the topsoil (0-23cm) of selected  treatments of the Hoosfield experiment, where spring barley has been grown each  
year since 1852. Data has been calculated from measured % SOC (0-23 cm) and standard soil weights, adjusted for observed decreases in top soil bulk density of plots given FYM, by including the appropriate amount of subsoil to ensure  soil weights were comparable over time. SOC has remained almost constant in the unfertilized plot  since the 1880s, at the equilibrium level for this farming system on this soil  type. 
Annual applications of 35 t ha<sup>-1</sup> FYM increased SOC rapidly at  first, then more slowly. This treatment now contains around three times as much SOC as the unfertilized plot. The third treatment, given FYM for just 20 years  between 1852 and 1871, still contains over 40% more SOC than the unfertilized plot.</P>
<p><strong>CROPPING AND TREATMENTS</strong></P>
<p><strong>Unfertilized:</strong> No fertilizer or organic manure since 1852.<br />
  <strong>FYM:</strong> 35 t ha<sup>-1</sup> FYM each year since 1852.<br />
<strong>FYM 1852-1871:</strong> 35 t ha<sup>-1</sup> FYM each year since 1852.</P>
<p>Spring barley grown every year, except 1912, 1933, 1943 and 1967 when the whole experiment was fallowed to control weeds.</P>
