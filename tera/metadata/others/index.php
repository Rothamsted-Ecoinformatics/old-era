<H2>Highfield Bare Fallow, Rothamsted</H2>

<p>
	In 1959 an area of permanent grass (since 1838, Lawes & Gilbert, 1885) adjacent to the
	Highfield Ley-Arable experiment at Rothamsted was ploughed, and has not
	grown a crop since. This is the <b>Highfield Bare Fallow</b>. It is
	kept free of weeds by frequent cultivation, but herbicides are used
	occasionally, so inputs of carbon to the soil are negligible. Soil
	organic carbon (SOC) is measured periodically, and this has declined
	substantially since the area was first ploughed out of grass.  Archived soil samples are available.
</p>

