<p><strong>System of replication:</strong> 4 randomised blocks of 12 plots each.<br />
  <strong>Area of each plots:</strong> 1/80 acre (30lks. X 41.7 lks.).<br />
  <strong>Treatments</strong>: No fumigant (O), single (1) and double (2) dressings of cymag (CM), carbon disulphide jelly (S), chlordinitrobenzene (N) and seekay (CK), at the following rates of application per acre for the single dressing: 1 CM, 7.5 cwt.; 1 S, 24.3 cwt.; 1 N, 2.0 cwt. and 1 CK, 5.0 cwt.<br />
  <strong>Basal Manuring: </strong>1 cwt. sulphate of ammonia per acre.<br />
  <strong>Cultivations: </strong>Ploughed: March 21 and 22. Fumigants ploughed in. Harrowed: March 25 and 26. Rolled:  March 26. Drilled: March 26. Harrowed: March 27. Rolled: March 27. Harrowed: May 3. Harvested: August 9. Variety: Marvellous. Previous crop: Spring oats.<br />
  <strong>Special Note:</strong> The ratio of weeds to total oats was determined by sampling at harvesting, two random samples being taken per plot.<br />
  <strong>Standard Errors per plot:</strong> Grain: 3.38 cwt. per acre or 28.5%; straw: 6.17 cwt. per acre or 30.4%; weeds: 5.07 cwt. per acre.</p>
