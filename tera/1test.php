								<H2>Woburn Ley-Arable</H2>

								<div align="center">
									<img src="metadata/others/Woburn_ley_arable_675.jpg"
										title="Woburn Ley Arable" width="675" height="278"
										alt="Woburn Ley Arable CS427" />

									<p>
										<B>Current design:</b> Five year rotation of three years in
										arable crops or grass leys followed by two years of test crops
										(wheat and barley). Total of 80 plots divided into 5 blocks.
										Yields are recorded every year and one block is soil sampled
										each year. One block limed each year.
									</p>
									<p>
										<b>Strengths:</b> Cropping data and yields available since
										1938. Soil samples available from 1938, and then every 5 years
										since the mid-1950s. Only long-term ley-arable cropping
										experiment based on the sandy loam at Woburn. Provides a
										comparison with the two ley-arable experiments at Rothamsted.
									</p>

									<p>
										<b>Background: </b> started in 1938 to compare the effects of
										rotations with or without grass or grass-clover leys on SOM
										and the yield of two arable test crops. Soils at Woburn that
										have been in continuous arable cropping since 1876 contain
										about 0.9 % C, and %C is still declining slowly; soils that
										have alternated between 3-year leys and 2-years arable since
										1938 contain about 1.2 % C. Typically, where no fertilizer N
										is applied, grain yields of the first wheat test crop are
										greater following grass leys than in the continuous arable
										sequence because more N is available from the mineralisation
										of SOM. Following grass-clover leys, yield is increased
										further because of the extra N being made available from the
										breakdown of the leguminous residues. Following the leys, a
										larger yield is often achieved, with less fertilizer N,
										compared with continuous arable cropping.
									</p>

