<h2>Description</h2>
<p>Rothamsted has  one of the longest running continuous sets of meteorological recordings in the world. The  figure shows the annual mean air temperature at Rothamsted every year from 1878-2019 (white circles). Also shown is the mean over each five-year period, 1878-1882,  1883-1887, etc (filled circles). The red line shows the mean temperature, 1878-1987. Mean annual  air temperature has fluctuated considerably between 1878-2019. However, when  the variation in annual temperature is smoothed using an 11-year (132 month) moving average (green dashed-line), it is  apparent that air temperatures have risen sharply since 1987 (Scott, 2015). An 11-year moving average removes solar variability and shorter-term variability such as the Southern Oscillation (Hansen et. al., 2010). The  mean air temperature at Rothamsted in 2019 was 10.48&deg;C which is  1.42&deg;C higher than the 1878-1987 average of 9.06&deg;C. The warmest year on record was 2014 with a mean annual temperature of 11.23&deg;C. The 10 warmest years on record occurred  in the last 20 years. Much of the rise is due to increases in the autumn and  winter months, and in night time temperatures.</p>
<p><strong>CLIMATE WARMING STRIPES</strong></p>
<p>This annual data, 1878-2019, is plotted visually with each years' temperature  represented by a coloured line going from deep blue (coldest) to deep red  (hottest) in relation to the long-term mean (white). The deeper the red, the  higher the temperature the darker the blue the colder the temperature. The  recent steep increase in temperature since the late 1980s is illustrated by the concentration of red  stripes to the right-hand side (visualisation concept created by Professor Ed Hawkins, University of Reading).</p>
