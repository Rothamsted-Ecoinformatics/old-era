<p align="justify"><a href=" Broadbalk/bbk_open_access_olsenp" target="_blank"><img class="intext" src="home/tyOABKolsenP.jpg" title="Broadbalk Open Access Olsen P" width="250" height="160" align="left" hspace="5" vspace="5" /></a><strong>Changes in plant-available P (Olsen P) in the topsoil</strong> (0-23cm) of selected plots of the Broadbalk wheat experiment since 1843. The accumulation of available P was greatest on the FYM plot, reflecting the larger total P inputs in FYM compared to that applied as inorganic fertilizer or where no P was applied. The larger Olsen P concentrations on plot 5 (PKMg) compared to plot 8 (N3PKMg), despite their similar P inputs, were a result of the higher yields and greater P offtakes in the crop on plot 8, due to the applied N. The relatively low Olsen P concentrations on plot 18 (N2 1/2PKMg) were a result of the limited P inputs between 1844 and 2001. Olsen P concentrations were least on plots receiving no (or very little) P inputs in fertilizer (plot 3, given no fertilizer or manure). </p>
