<p> <b><a  href="<?=$det?>" target="out">16-01-2019: Update to the e-RA Data Extraction Tool</a>: </b> 
 The update solves an issue we had with the values calculated after extraction of Met Data and the consistency in displaying the <b>RAD(calc)</b> value across all the weather datasets (BROOMET, ROTHMET, and WOBMET). 
 When extracting data from these datasets, the DET provides an option to calculate <a href="Met/derived_variables">derived variables</a>, one of which is <b>RAD</b>, radiation. b>RAD</b> is measured in the three weather 
 stations, but when it is missing for technical reasons, the ‘calculating tool’ derives it from SUN hours. 
The column <b>RAD</b>(calc) shows the value used in the calculations of the other derived variables (evaporation for example). It can be either RAD measured or RAD derived from SUN hours.  
The latter is now displayed in a red font to make it clear that this value is calculated from SUN hours. The value of RAD is converted to MJ/m<sup>2</sup> in the following calculations from SUN hours, 
however, the value of RAD(calc) is displayed in the same unit as the value of RAD obtained from the dataset, which can be MJ/m<sup>2</sup> for BROOMET and WOBMET, or J/m<sup>2</sup> for ROTHMET. </p>
