<h2>Description</h2>
<p><strong>Numbers of eelworms in soil before and after fumigant treatments - Residual effects</strong>. This dataset consists of the plan, treatments, cultivations, yields and numbers of nematode eelworm cysts relating to the fumigation Experiment conducted in the field, Pastures, at Rothamsted Experimental Station, in 1936, following on from 1935. Two random samples of 50 grms soil were taken per half plot Dec 1 and 2 and third cysts count made.</p>
<p>This experiment is originally documented in the Rothamsted Annual Report of 1936 pages 211-212 as digitally archived in eRADoc; Other Experiments at Rothamsted (1937) Report For 1936, pp 211-212 - DOI: 
  <a href="https://doi.org/10.23637/ERADOC-1-68">https://doi.org/10.23637/ERADOC-1-68</a>.</p>
<p>See <a href="http://www.era.rothamsted.ac.uk/Other/FumigationExperiment1935" target="_blank">Soil Fumigation Experiment 1935</a> for the next years results.</p>
<h2>Use as an agricultural dataset for statistical techniques</h2>
<p>The first part of this experiment, <a href="http://www.era.rothamsted.ac.uk/Other/FumigationExperiment1935" target="_blank">Soil Fumigation Experiment 1935</a>, was used as the source of data and information for agricultural data package in Package 'agridat' July 2018 page 108 &quot;cochran.eelworms; <em>Counts of eelworms before and after fumigant treatments</em>'' <a href="https://cran.r-project.org/web/packages/agridat/agridat.pdf" target="_blank">https://cran.r-project.org/web/packages/agridat/agridat.pdf</a> which in turn was sourced from Cochran and Cox, 1950 Experimental Designs. Table 3.1. This second part of the experiment provides extra data for analysis from a third cyst count in relation to fumigant treatments.</p>
<p>This dataset  consists of 48 observations with 4 randomised blocks of 12 treatment plots. The yields in the Tables and Summary Tables are in different quantities; the conversion factors for cwt/acre to tonnes/ha is 0.1255, for cwt/acre to kg/ha is 125.5 and for lb/acre to kg/ha is 1.121.</p>
