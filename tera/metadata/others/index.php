<H2>Saxmundham Rotations I & II</H2>

<p> Saxmundham Experimental Station is in East Suffolk,  five miles (8 km) from the sea and 110 miles (177 km) East of Rothamsted. The soil is a heavy sandy clay 
loam which can be difficult to cultivate, providing a contrast with the soils at Rothamsted and Woburn.  Two long-term experiments were started in
1899 by East Suffolk County Council. Each consisted of four blocks so that a typical Norfolk four-course rotation could be grown, with each crop 
present each year. Rothamsted assumed responsibility for the site in 1965 and the experiments were reviewed and modified. Rothamsted relinquished the site in 2010. </p>
<p>Rotation I experiment was a factorial test of N, P and K plus bone meal and FYM treatments (Williams & Cooke, 1971). Since 1965 the experiment
has been used to look at crop responses to P and K and their interactions with N, particularly with high rates of N applied to high yielding wheat cultivars for bread-making quality. </p>
<p>Rotation II experiment looked at how limited amounts of FYM, sodium nitrate and superphosphate could best be used over the four-course rotation. Since 1965 the experiment has 
been used to look at the response of various crops to fresh and residual P (Johnston et al, 2013) and the decline in plant-available P when fertilizer P was withheld (Johnston et al, 2016). 
The critical level, above which there is no further response to fresh P, is higher and more variable on this heavier soil than on the better soil at Rothamsted 
(see Exhaustion Land). </p>
