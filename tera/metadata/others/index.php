<A NAME="SEC8"></a>
<H2>Highfield Conversion experiment, Rothamsted</H2>

<p>
	<b>Design:</b> In 2008 two plots (continuous grass and arable) within
	each block of the Highfield Ley-Arable experiment were split to establish grass,
	arable and bare-fallow treatments within each. Adjacent long-term bare-fallow
	areas were also included in the experiment. Soil samples are taken
	regularly and yields are taken annually. Many archived samples are available.  
        Baseline soil measurements before the Conversion are given in Hirsch et al (2009);
        changes that have occurred since the Conversion are described in Hirsch et al (2017).

</p>

<p>
	<b>Strengths:</b> It is the only site at Rothamsted available for studying
        the effects of converting long-term grass to arable and bare-fallow treatments
        on SOC, fertility, soil resilience and recovery. The experiment has support from several groups within
	Rothamsted, providing a resource for multidisciplinary research, from 
        analytical chemistry, soil physical structure, GHG emissions and nitrification potential,
        microbial and mesofauna community responses to management change. It is probably unique in
	that it examines soil resilience and restoration through plants.
</p>


