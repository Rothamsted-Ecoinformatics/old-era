<A NAME="SEC3"></a>
<H2>Garden Clover, Rothamsted</H2>

<div align="center">
	<img src="<?=$metadata?>1921_Garden_Clover_675_crop.jpg"
		title="Garden Clover 1921" width="675" height="252"
		alt="Garden Clover 1921" />
</div>
<p>
	<B>Current design:</b> Four plots (each 1 x 1.4m) growing continuous
	red clover (<i>Trifolium pratense</i>), reseeded every 5-6 years. Lime
	and fertilizers (P, K and Mg) are applied in autumn. Vegetation is
	harvested three times a year.
</p>

<p>
	<b>Strengths:</b> The only remaining Classical experiment devoted
	specifically to the study of a legume, and the oldest experiment of its
	kind in the world. Long-term data on clover yields and many archived
	samples (from about 1862) available. Soil samples, which show a decline
	in total N of >50% since 1857, are also available.
</p>

<p>
	<b>Background:</b> the smallest and simplest of the Classical
	experiments, with (until 1956) only one, unmanured plot measuring 3m by
	2.1m. The experiment was established in the Manor Garden in 1854 to see
	whether red clover could be grown continuously on a rich garden soil.
	Yields were very large for the first 10 years averaging about 10 t dry
	matter ha<sup>-1</sup>, probably because the soil was rich in nutrients
	and the soil-borne pests and diseases of clover were absent. Reasonable
	yields were obtained over the next 30 years but thereafter yields
	showed a marked decline and there were several complete failures.
</p>

<p>
	Between 1956 and 1972 the plot was sub-divided and a sequence of tests
	made of K, molybdenum (Mo), formalin, N and Mg, but the the crop was
	usually severely damaged during the winter by clover rot (<i>Sclerotinia
		trifoliorum</i>) and was resown each spring. Since 1973 basal N, P, K,
	Mg and chalk have been applied.
</p>

<p>
	Between 1976 and 1978 aldicarb was tested as a control for clover cyst
	nematode, <i>Heterodera trifolii</i>, which was known to be present,
	and the cultivar Hungaropoly, believed resistant to clover-rot, was
	compared with the standard susceptible variety S.123.
</p>

<p>
	The plot then grew Hungaropoly only, with basal aldicarb (until 1988),
	and tested the fungicide benomyl from 1980-90. The cultivar was changed
	to Merviot in 1996. Between 1979 and 2006 the experiment has been
	resown seven times. A mean yield of 13t ha<sup>-1</sup> has been
	achieved in this period, with up to 20 t ha<sup>-1</sup> in some years.
	In 2013 the clover variety was changed to Milvus
</p>

<p>Other than Park Grass, with its mixed herbage, this is the only
	Classical experiment where a non-graminaceous crop has been grown
	continuously. In terms of microbial diversity, its soil provides a
	potentially valuable contrast with those of Broadbalk and Hoosfield.</p>

<p>
	Background taken from the <i><a href="home/Web_LTE_Guidebook_2018_2019-reprint.pdf" target="_blank">Rothamsted Guide to the Long-Term
Experiments</a></i><a href="home/Web_LTE Guidebook_2019.pdf"> 2018</a>, page 36. </p>


