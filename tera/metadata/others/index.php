<H2>Woburn Market Garden experiment</H2>

<div align="center">
	<img src="<?=$metadata?>Woburn_Market_Garden 1_675.jpg"
		title="Woburn Market Garden" width="675" height="284"
		alt="Woburn Market Garden" />
</div>
<p>
	The <b>Market Garden experiment</b> started in 1942, originally to look
	at the effects on SOM and crop yield of various organic inputs; namely
	FYM, compost and sewage sludge. The experiment was grass from 1974 to
	1982. When concerns were expressed in the late 1970s about the heavy
	metal content of sewage sludges being applied to agricultural land, the
	experiment was “reactivated” to examine the fate of metals that had
	been applied in the sewage sludge between 1942 and 1961. More recently,
	the experiment has been used to evaluate the effectiveness of
	hyperaccumulator plants, i.e. plants that can naturally accumulate
	large amounts of metals from soils and which, potentially, could be
	used to “clean up” soils contaminated with heavy metals. Part of the
	experiment was physically moved in 2005 and in 2006 the whole
	experiment was sown to grass.
</p>

