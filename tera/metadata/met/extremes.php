    <h2><strong><A NAME="SEC1"></a>Extreme Days </strong></h2>
  </div>
  <h1> <b>Hottest day</b></h1>
  <p> The highest temperature at Rothamsted (Harpenden), since records began  in January 1878, was <b>36.7 </b>&ordm;C on <strong>July 25th 2019</strong>.
    This beats the previous record of 35.6 &ordm;C on 10th August 2003.  
    July 25th 2019  was also the hottest day at Woburn in Bedfordshire, with <b>37.5 </b>&ordm;C, and at Brooms Barn in Suffolk, 
    with <b>36.6</b> &ordm;C, both beating previous records on 10th August 2003.
  <p><em>The 2003 European heat wave led to the hottest summer on  record in Europe since at least 1540. Several weather records were broken in the United Kingdom,  including the UK's highest recorded temperature 38.5 &ordm;C (101.3 &ordm;F) at Faversham  in Kent on 10th August (Wikipedia).</em>
  <h1> <b>Coldest day</b></h1>
  <p> The lowest temperature, since records began  in January 1878, was -17.0 &ordm;C on <strong>13th December 1981</strong>.
  <p>    <em>The winter of 1981-82 in the United Kingdom was called &quot;The  Big Snow of 1982&quot; by the press (Wikipedia).</em></p>
  <h1><b>Wettest day</b></h1>
  <p> The wettest day, since records began in February 1853, with 3.238 inches (82.2mm) rain was <strong>26th June 1888</strong>.
  <P><em> The <a href="https://www.thetimes.co.uk/article/weather-eye-the-snowy-summer-of-1888-n02d3xlkgv3" target="_blank">Times reported on July 12 1888</a>: &ldquo;Extraordinary weather for the  time of year is reported from various parts of the country&quot; with snow in the summer!</em></p>
