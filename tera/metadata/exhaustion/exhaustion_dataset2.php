<h1>Description</h1>
<p>This  dataset represents  the monthly mean rainfall at Rothamsted continuously from March 1853 - July 2018  and is based on continuous daily records measured at the site.</p>
<p><strong>MEASUREMENTS</strong></p>
<p>Daily rainfall is measured as the total (mm) over the 24-hour period 0900 to 0900 GMT. It includes all precipitation - snow, rain, mist and fog. 
 Rainfall was first recorded at Rothamsted in 1853, using a copper funnel rain gauge (5 inch / 12.7 cm diameter) and measured using a graduated cylinder. Since 2004 it has been measured using an electronic tipping bucket rain gauge (10 inch / 25.4cm diameter), ARG100, calibrated to tip at 0.2mm (which has since which become the minimum amount of rain that can be recorded). The rain gauge is placed within a 30cm deep 1.5m radius turf wall, retained by brick, to reduce wind eddies that may potentially blow  rain out of the gauges. Data were collected daily manually until 2004 and since then by Automatic Weather Station using a standard protocol<strong>.</strong></p>
<p><strong>LOCATION</strong></p>
<p>Rothamsted Meteorological Station, Harpenden, Hertfordshire, England 51.82 N 0.37 W 128 m asl. </p>
<h1>Provenance &amp; Quality</h1>
<p>The monthly summary data contained in this spreadsheet are derived from  daily data measured at Rothamsted Meteorological Station, Harpenden. Mean monthly data is determined from daily data using  Genstat 19th Edition. This dataset is comprised of a composite of RAIN and RAIN5, based on RAIN, with data from RAIN5 being used from 1880-1914 only (see <a href="http://www.era.rothamsted.ac.uk/Met/instrumentdescription#SEC1" target="_blank">Rainfall</a>). Verification includes checks for instrument errors, for missing data and outliers. Missing values are acquired form the Met Office Station on site. The original raw daily data is available, after registering, from the e-RA database. Please contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a> for an access password and further details.</p>
