<h1>Provenance &amp; Quality</h1>
<p>Data from plots 2b, 3&4, 5, 10, 11, 12, 13, 14, 17 and 18 were transcribed
from Fisher's original hand written datasheets. These are kept in the Rothamsted Research Library Archive. The original datasheets for plots 6-8 could not be found in the Archive, so the annual data have been 
re-constructed, based on yield data in the e-RA Database, dataset BKYIELD. See Supporting Information for more details of how this was done. Verification includes checking all data entry, checking that the
mean values agree with Fisher's mean values in Table 1 of the 1921 paper and re-constructing known data from plots 5 and 11 to check which sub-plots were used by Fisher. There are some minor differences, see 
Supporting Information for more details. The raw data used to derive the yields for plots 6-8 is
available, after registering, from the e-RA database. Please contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a> for an access password and further details.</p>


