<h1>Description</h1>
<p>This dataset consists of  monthly summaries of meteorological data measured at Saxmundham Experimental Station in East Suffolk, UK, from January 1966 until December 1985,
and is derived from daily measurements taken at the site. </p>
<p><strong>MEASUREMENTS</strong></p>
<p><li><b>Mean monthly air temperature:</b> the mean of the maximum and minimum air temperature (degrees C);</li>
<li><b>Mean monthly dew point: </b> derived from wet and dry bulb temperature, measured at 0900 GMT (degrees C); </li>
<li><b>Mean monthly soil temperature:</b> the soil temperature in ground under bare soil at 30cm depth, measured at 0900 GMT (degrees C); </li>
<li><b>Total monthly ground frosts:</b> the number of nights when the grass minimum temperature was below 0 degrees C (days per month);</li>
<li><b>Total monthly rainfall: </b>including all precipitation (snow, rain, mist and fog) captured in a 12.7 cm rain gauge (mm per month); </li>
<li><b>Total monthly rain days:</b> the number of days rainfall was 0.2mm or more (days per month); </li>
<li><b>Mean monthly wind speed:</b> measured at 2 metres above ground level, average over 24 hours (km per hour); </li>
<li><b>Mean daily solar radiation:</b> measured with a Kipp Solarimeter (MJ m<sup>-2</sup> day<sup>-1</sup> ); </li>
<li><b>Total monthly evaporation over open water:</b> mean of two open water tanks installed in the spring of each year (mm per month).        </li>

<p><strong>LOCATION</strong></p>
<p>Saxmundham Experimental Station, Saxmundham, Suffolk, UK. 52.221 N 1.466 E 37 m asl. </p>
<h1>Provenance &amp; Quality</h1>
<p>The monthly summary data contained in this spreadsheet are derived from  daily data measured at Saxmundham Experimental Station Meteorological Station, Saxmundham, Suffolk, UK.
The 1966 and 1967 summaries were published in the Rothamsted Annual Reports for those years; subsequent years were also published in the 'Numerical Results' or
'Yields of the Fields Experiments' for 1968-1985, as well as the Rothamsted Annual Reports for each year.   
Summary data were converted from paper copies to electronic files using code to extract and compile summary data into
a single dataset, July 2019.
Verification of daily data includes checks for instrument errors, for missing data and outliers. </p>
