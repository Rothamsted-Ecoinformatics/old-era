<h2>Broadbalk Wheat:</h2>
<p><a href="Broadbalk/bbk_open_access_yields" target="_blank">Broadbalk Yields</a><br>
<a href=" Broadbalk/bbk_open_access_yields" target="_blank"><img class="intext" src="home/OABKyieldty.jpg" title="Broadbalk Open Access Yields" width="250" height="170" align="left" hspace="5" vspace="5" /></a></p>

<p align="justify"><strong>Mean long-term winter wheat yields</strong> from selected treatments   on Broadbalk are shown. The changes reflect the improved cultivars, 
cultivations and   control of pests, diseases and weeds that have been introduced on Broadbalk,   especially since the 1960s. Yields of continuous   
wheat given no fertilizer or manure have remained at around 1 t ha <sup>-1</sup>.   Since 1979 summer fungicides have been used, which has allowed us to exploit the   greater grain 
yield potential of modern cultivars. In 1968 a rotation was   introduced on part of the experiment, so that it is now possible to compare the   yields of wheat grown 
continuously and as the first wheat after a two year   break. The highest yields are now from the first wheat crop in rotation, with   the best yields from fertilizer alone exceeding those from FYM alone, and the   
combination of FYM + 96 kgN ha<sup>-1</sup> (144 kgN ha<sup>-1</sup> since 2005)   often exceeding both.</p>
<p align="justify"><br clear="all" />

<p>  <a href=" Broadbalk/bbk_open_access_soc" target="_blank">Broadbalk Soil Organic Carbon</a></p>
<p align="justify"><a href=" Broadbalk/bbk_open_access_soc" target="_blank"><img class="intext" src="home/tyOABKsoc.jpg" title="Broadbalk Open Access Soil Organic Carbon" 
width="250" height="170" align="left" hspace="5" vspace="5" /></a><strong>Changes in soil organic carbon (SOC)</strong> content in selected   treatments of the Broadbalk experiment, where winter wheat has been grown each year 
  since autumn 1843. SOC (0-23cm) has remained almost constant in the unfertilized plot, at the equilibrium level for this farming system on this soil type. Inorganic fertilizer (NPK) has enhanced SOC a little, 
  probably due to increased returns of organic matter in crop roots and residues. The treatment given 35 t ha<sup>-1</sup> FYM now contains almost three times as much SOC as the unfertilized plot. Increases were greatest 
in the initial years of the experiment. </p>
