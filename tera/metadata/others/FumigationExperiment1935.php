<p><strong>Conclusions</strong></p>
<p>Carbon disulphide jelly and cymag produced significant increases in the yield of grain, cymag giving slightly, but not significantly, higher yields than carbon disulphide jelly. In neither case was the falling-off in response at the higher level of dressing significant. The responses were presumably due in part at least to nitrogen, the single dressing of carbon disulphide jelly and cymag being equivalent to 37 and 87 lb. nitrogen per acre respectively. Chlordinitrobenzene significantly decreased the yield, but there was no apparent difference between the effects of the single and double dressings. There was practically no crop on the plots receiving seekay, due to the short interval between application of the fumigants and the drilling of the seed.
<p>No relation was found between the yields and the numbers of cysts at the second eelworm count after eliminating treatment effects.
<p>The results for straw were similar to those for grain.
<p>All fumigants produced large decreases in the weight of weeds, with a further decrease with the double dressing. The crop was unusually weedy.
