<h2>Park Grass:</h2>
<p><a href=" Park/pg_open_access_species" target="_blank">Park Grass Species Diversity</a></p>
<p><a href=" Park/pg_open_access_species" target="_blank"><img class="intext" src="home/tyOAPGspecies.jpg" title="Park Grass Open Access Species" width="250" height="170" align="left" hspace="5" vspace="5" /></a></p>
<div align="justify"><strong>The</strong> <strong>number of species</strong> comprising 1% or more of the biomass in response to  selected treatments. Numbers of species have decreased, even on   the nil plots. This may be due to changes in atmospheric inputs and/or changes   in sward management. Applying N as sodium nitrate (N*) or ammonium sulphate (N)   reduces diversity further, and in the ammonium form also rapidly acidifies the   soil, reducing the number of species to one or two.   Applying chalk aids recovery from acidity. Withholding N causes more species to return (not shown).
</p>
  <br clear="all" />
</div>
<p><a href=" Park/pg_open_access_soilph" target="_blank">Park Grass Soil pH</a></p>
<p><a href=" Park/pg_open_access_soilph" target="_blank"><img class="intext" src="home/tyOAPGsoilpH.jpg" title="Park Grass Open Access Soil pH" width="250" height="170" align="left" hspace="5" vspace="5" /></a></p>
<div align="justify"><strong>The  changes in pH of the soil</strong> (at depth 0-23cm) on Park Grass in response to atmospheric deposition and fertilizer inputs, and then consequent management by liming. The pH was  approximately 5.5  when the experiment began.  A regular test of liming  began in 1903 with chalk applied every 4 years to the southern half of most  plots. Plots were then further sub-divided to give a further range of pHs. Soil pH  is c 3.5 on the plots given most ammonium  sulphate and 5.0-5.3 on the un-limed, unfertilized plots. Sub plot c of most  plots is nearest to the original soil pH. Soil pH has slowly recovered by about  0.5 pH units on some plots more than 25 years after ammonium sulphate was last  applied.
</p>
  <br clear="all" />
  
