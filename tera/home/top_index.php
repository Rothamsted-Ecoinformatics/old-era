<p>The data in e-RA are available for scientific research, but remain the property of Rothamsted Research and the Lawes Agricultural Trust. Most of the data are password-protected and access is subject to our <a href= "home/eap.pdf" target="_blank">Data Access Policy</a>. </p>

<p>The <a href="index.php?area=home&amp;page=open_access"target="_blank">Open Access</a> data are available to all, no password is required, although we ask that users acknowledge Rothamsted Research as the data source.</p>

<p><b>Please contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a> to arrange a password, for further information, or to extract data on your behalf.</b></p>


