  <p><strong>Note:</strong> Even earlier records, in the form of a weather diary (as opposed to actual measurements), were made by Sir John Wittewronge, an ancestor of Sir John Lawes who also lived at Rothamsted Manor. These weather records provide a fascinating insight into weather conditions over five years at the end of the 17th Century (1684-1689) and give an indication of the weather experienced in Hertfordshire at a time when the Little Ice Age was affecting Europe (<a href="https://archives.hertfordshire.gov.uk/product/observations-of-weather-the-weather-diary-of-sir-john-wittewronge-of-rothamsted-1684-1689-2/" target="_blank">&quot;'Observations of Weather' The Weather Diary of Sir John Wittewronge of Rothamsted 1684-1689&quot;.  Edited by Margaret Harcourt Williams and John Stevenson and published in 1999 by Hertfordshire Record Society Publications</a>).
  <p>*Some variables such as snow and cloud cover continued to be measured manually until May 2007.
