<h1>Description</h1>
<p>This  dataset represents  the mean monthly rainfall recorded at Rothamsted  from October 1986 - September 2018  and is derived from continuous daily records measured at the site. </p>
<p><strong>Publication</strong></p>
<p>This dataset was compiled for the publication J. Macholdt, H.-P. Piepho, B.  Honermeier, S. Perryman, A. Macdonald and P.  Poulton. The effects of cropping sequence, fertilisation and straw management on the yield stability of winter wheat (1986-2017) in the Broadbalk Wheat Experiment, Rothamsted, UK. (2020). Journal of Agricultural Science.</p>
<p>This publication also includes reference to the dataset <a href="http://www.era.rothamsted.ac.uk/Met/rmsMMT10860918" target="_blank">Mean monthly temperature at Rothamsted October 1985 - September 2017</a>.</p>
<p><strong>MEASUREMENTS</strong></p>
<p>Daily rainfall is measured as the total (mm) over the 24-hour period 0900 to 0900 GMT. It includes all precipitation - snow, rain, mist and fog. 
 Rainfall was first recorded at Rothamsted in March 1853, using a copper funnel rain gauge (5 inch / 12.7 cm diameter) and measured using a graduated cylinder. Since 2004 it has been measured using an electronic tipping bucket rain gauge (10 inch / 25.4cm diameter), ARG100, calibrated to tip at 0.2mm (which has since  become the minimum amount of rain that can be recorded). The rain gauge is placed within a 30cm deep 1.5m radius turf wall, retained by brick, to reduce wind eddies that may potentially blow  rain out of the gauges. Data were collected daily manually until 2004 and since then by Automatic Weather Station using a standard protocol<strong>.</strong> There are differences in the capture rate between the two gauges, see <a href="http://www.era.rothamsted.ac.uk/Met/instrumentdescription" target="_blank">Rainfall</a> for further information.</p>
