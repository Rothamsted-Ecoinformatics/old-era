<p> <a href="http://www.era.rothamsted.ac.uk/Met/broomet" target="out"> <b>01-07-2018 Position of the Brooms Barn met station and wind measurements</b></a>: The Brooms Barn meteorological station is approx. 30m from the main buildings, which are approx. 10m high. Tony Scott has reported that this may cause some interference with the measurement of wind speed and wind direction, as ideally a mast with wind sensors should be a minimum of 10 times the height of the nearest building away from the nearest building (ie at least 100m apart). This gives enough fetch for the wind to settle down.  The met station and main building have always been in these positions. Please contact the e-RA Curators for more details.</p>
