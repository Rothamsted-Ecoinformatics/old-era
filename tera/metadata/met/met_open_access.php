<h2>Other sources of Open Access Met Data:</h2>
</p>

<p>The <b>UK Environmental Change Network</B><a href="http://www.rothamsted.ac.uk/environmental-change-network"> (ECN)</a> provides many meteorological data variables measured at Rothamsted which can easily be downloaded. There are <a href= "http://resources.rothamsted.ac.uk/environmental-change-network/rothamsted-weather-charts">daily, monthly and annual </a>charts on many variables including maximum and minimum  air temperature, soil temperature, rain, radiation and wind speed and direction. These are based on weather variables collected on an hourly basis.</p>

<p><a href="http://resources.rothamsted.ac.uk/environmental-change-network/yearly-weather-summaries">Yearly and monthly weather summaries</a> are provided since 2001. These show: 

<ul>
<li>Sunshine (hours per month)</li>
<li>Mean maximum air temperature (C) </li>
<li>Mean minimum air temperature (C) </li>
<li>Ground frosts (number per month) </li>
<li>Mean soil temperature at 10cm under bare soil (C) </li>
<li>Mean soil temperature at 30cm under grass (C) </li>
<li>Total monthly rainfall (mm) </li>
<li>Rainfall duration (days per month) </li>
</ul></p>
The departure from the 30 year mean (1981-2010) is also shown for sunshine, maximum and minimum temperatures and rainfall.
