<H1>Open Access Met Data </H1>
<p>Selected data from the Rothamsted long term experiments is being made freely available as prepared summaries of commonly requested data. This recognises the national and international importance of the data. The aim is that greater use of the data will lead to further understanding and wider benefits. 
Rothamsted relies on the integrity of users to ensure that Rothamsted Research receives suitable acknowledgment as being the originators of these data. Please contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a>  for help with questions about the data or its interpretation.
</p>
<p>We also hope that the Open Access Data will be widely used by <b>schools, colleges and universities</b>, as a teaching resource, and would welcome any feedback on this. </p>
<p>The following Meteorological Open Access Datasets are currently available:</p>
