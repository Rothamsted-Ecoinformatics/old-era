<p>Pollution studies/understanding leaching</p>
<table width="631" border="2">
  <tr>
    <td width="262" height="200">&nbsp;</td>
    <td width="351"><strong>Minimising  P pollution:</strong> Studies from the Broadbalk experiment were the first to recognize  the phenomena of P leaching, mainly through macro-pore flow, which avoids  contact with a large surface area of soil (Heckrath et al, 1995). This work  demonstrated that P losses were low, until Olsen P reached a certain value, the <em>change point </em>after which losses  increased greatly. The change point varies between soils. <strong></strong>      <ol>
    </ol></td>
  </tr>
  <tr>
    <td>&nbsp;</td>
    <td>Atmospheric pollutants:The  archived soil and plant samples have allowed the retrospective analyses of  pollutants, such as dioxins and poly-aromatic hydrocarbons, which have  increased in the atmosphere since the early 1900s, and for cadmium following  the application of phosphate fertilizer. 
      <ol>
    </ol></td>
  </tr>
  <tr>
    <td height="200">&nbsp;</td>
    <td><strong>Methane:</strong> Research at several long-term experiments, including Broadbalk and Park Grass,  has led to greater understanding about the ability of aerobic soils to act as a  sink for the greenhouse gas methane (Stiehl-Braun et al, 2011).
<ol>
</ol></td>
  </tr>
  <tr>
    <td>&nbsp;</td>
    <td>Uranium  and Plutonium contamination: Scientists from Southampton Oceanography Centre  analysed archived samples of herbage from Park Grass over 50 years to measure  concentrations of uranium and plutonium. They were able to detect the effects  of, and distinguish between, nuclear bomb tests carried out by the US, USSR, UK  and France, and show that plutonium contamination from weapons testing the  Nevada Desert in 1952/53 reached Northern Europe. Such measurements have only  become possible in recent years with the development of more sophisticated  analytical techniques (Warneke et al, 2002). <strong></strong></td>
