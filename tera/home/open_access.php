<h2>Broadbalk & Geescroft Wilderness Experiments:</h2>
<p align="justify"><strong>The accumulation of organic carbon in soil and tree biomass</strong> has been measured on two contrasting sites at Rothamsted that were fenced off in the 1880s and allowed to revert 
naturally to woodland. Both sites had grown arable crops for many years before. 
  Although not experiments in the usual sense, these two areas of regenerating woodland are of great value, providing information on the long-term sequestration of carbon in soil and vegetation. </p>
</p>
<p><a href=" Wilderness/wilderness_open_access_broadbalk_oc" target="_blank">Accumulation of organic carbon in soil and trees in Broadbalk Wilderness</a></p>
<p><a href=" Wilderness/wilderness_open_access_broadbalk_oc" target="_blank"><img class="intext" src="home/tyOABKWsoc.jpg" title="Broadbalk Wilderness Open Access SOC" width="250" height="170" align="left" hspace="5" vspace="5" /></a>
<div align="justify"> The <b>Broadbalk</b> site grew unmanured winter wheat since 1843. Since 1882 it has been allowed to naturally revert to woodland. Large amounts of chalk had been applied, and the surface soil pH is still neutral (pH 7.7). <br clear="all" />
</div>
<p><a href=" Wilderness/wilderness_open_access_geescroft_oc" target="_blank">Accumulation of organic carbon in soil and trees in Geescroft Wilderness</a></p>
<p><a href=" Wilderness/wilderness_open_access_geescroft_oc" target="_blank"><img class="intext" src="home/tyOAGEWsoc.jpg" title="Geescroft Wilderness Open Access SOC" width="250" height="170" align="left" hspace="5" vspace="5" /></a> The larger <b>Geescroft</b> site grew beans (<i>Vicia faba</i>) for many years, and has been left uncultivated since 1883. It did not recieve as much chalk as the Broadbalk site, and the soil is now acidic (pH 4.4 in 1999). <br clear="all" />
  
