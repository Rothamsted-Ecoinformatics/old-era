<h2>Description</h2>
<p><strong>Numbers of eelworms in soil before and after fumigant treatments</strong>. This dataset consists of the plan, treatments, cultivations, yields and numbers of nematode eelworm cysts relating to the Fumigation Experiment conducted in the field, Pastures, at Rothamsted  Experimental Station in 1935. In March 1935, 400 grams of soil were sampled and the number of eelworm cysts were counted. Fumigants were added to the soil, oats were sown and harvested in August. In October the plots were sampled again and the final number of cysts recorded. </p>
<p>This experiment is originally documented in the Rothamsted Annual Report of 1935 pages 176-178 as digitally archived in eRADoc &quot;Other Experiments at Rothamsted&quot; (1936) Report for 1935,  DOI:  <a href="https://doi.org/10.23637/ERADOC-1-67">https://doi.org/10.23637/ERADOC-1-67</a></p>
<p>See <a href="http://www.era.rothamsted.ac.uk/Other/FumigationExperiment1936" target="_blank">Soil Fumigation Experiment 1936</a> for the next years results.</p>
<h2>Use as an agricultural dataset for statistical techniques</h2>
<p>This experiment was used as the source of data and information for agricultural data package in Package 'agridat' July 2018 page 108 &quot;cochran.eelworms; <em>Counts of eelworms before and after fumigant treatments</em>'' <a href="https://cran.r-project.org/web/packages/agridat/agridat.pdf" target="_blank">https://cran.r-project.org/web/packages/agridat/agridat.pdf</a> which in turn was sourced from Cochran and Cox, 1950 Experimental Designs. Table 3.1.</p>
<p>This dataset consists of 48 observations with 4 randomised blocks of 12 treatment plots. The yields in the Tables and Summary Tables are in different quantities; the conversion factors for cwt/acre to tonnes/ha is 0.1255, for cwt/acre to kg/ha is 125.5 and for lb/acre to kg/ha is 1.121.</p>
