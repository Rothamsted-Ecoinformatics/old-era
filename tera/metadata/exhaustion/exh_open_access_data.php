<p><strong>SOIL SAMPLING AND ANALYSIS</strong></p>
<p>  <strong>Crop yields</strong> grain and straw yields were determined at 85% dry matter</p>
<p><strong>Soil sampling</strong> Samples were taken from the  topsoil (0-23&nbsp;cm), usually in autumn. 16-20 cores were taken with a  2.0&nbsp;cm diameter semi-cylindrical auger using a random zigzag pattern up and down  the length of each sub-plot. Using the known soil weight of topsoil, Olsen P,  in mg P/kg, can be converted to kg P/ha to relate changes in the amount of  Olsen P to the amount of P removed from the soil (Johnston et al., 2016). A  range of Olsen P concentrations were established between 1856 and 1901 when P  was added as fertilizer or in FYM. Following a long period of &lsquo;exhaustion&rsquo;, different  rates of P were applied to subplots between 1986 and 1992, and &lsquo;maintenance&rsquo;  dressings have been applied since 2000. However, some sub-plots have received  no fresh P since 1901 or 1856. Before the 1980s soils were only sampled  occasionally, since then they have been sampled every two or three years. For those  years when the soil was not sampled, the crop response to plant-available P was  correlated with the average Olsen P data from the years before and after. </p>
<p><strong>Plant-available  'Olsen' P </strong>is determined by the  Olsen method, (Olsen et al., 1954). Air dried soils (2mm) &nbsp;are extracted with 0.5M NaHCO3,  buffered at pH 8.5. Extracts are analysed colourimetrically using the Technicon AAII  segmented continuous flow analyser. Data for 1985 and 1992 calculated; for P3, P2 &amp; P1 plots in 1992 a linear increase between 1989 &amp; 92 is assumed; P0 plots are mean of 1991 &amp; 93. Plots 031 in 1993, 032 in 1996, and 072  in 1999 &amp; 2001 are repeat analyses.</p>
<p><strong>Total soil P</strong> was sampled as half-plots in 1974; equivalent to current sub-plots 1&amp;2 and 3&amp;4; re-analysed by Aqua regia in 2008. It is determined by fusion with NaCO3  (Mattingly, 1970) and analysed colourimetrically (modified Murphy and Riley, molybdenum  blue) on the Technicon AAII segmented flow analyser, or by Aqua Regia digestion (McGrath &amp; Cunliffe, 1985). Digested solutions are analysed using a Perkin Elmer Optima -  Inductively Coupled Plasma Optical Emission Spectrometry (ICP-OES).  Note: The data is very limited, in 1974 the soil samples were taken from  half-plots rather than the quarter-plots that were introduced in 1976.</p>
<p><strong>% P in grain and straw</strong> is determined by dry  ashing (Piper, 1942) using a muffle furnace at 450-500 ºC. Solutions were analysed  colourimetrically using &nbsp;the modified Murphy and Riley, molybdenum blue  method on the Technicon AAII segmented flow analyser.&nbsp; P, K, Ca, Mg and Na  were determined using automated Atomic Absorption (AA). Since 1975 these have been done using ICP-OES analyses. </p>
<p><strong>P uptake in grain and straw</strong> were calculated from  the crop yield  at 100% dry matter and %P in the grain and straw.</p>
