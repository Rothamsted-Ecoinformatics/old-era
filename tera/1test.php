			<A NAME="SEC2"></a>
			<H2>Agdell, Rothamsted</H2>

			<div align="center">
				<img src="metadata/others/Former_Agdell_2015_675.jpg"
					title="The former Agdell" width="675" height="252"
					alt="The former Agdell" />

				<p>
					<b>Agdell</b> was the only Classical Experiment in which crops were
					originally grown in rotation. From 1848 to 1951, three different
					manurial combinations (none, PKNaMg and NPKNaMg plus rape cake,
					castor meal) were applied to the root crops of two four-course
					rotations. The rotations differed only in their third course -
					roots, barley, fallow or legume, wheat. There were only six plots
					and only one course of the rotation was present each year. The root
					crop was turnips or swedes, the legume clover or beans. From 1920,
					club-root (<i>Plasmodiophora brassicae</i>) became progressively
					more damaging to the root crop, especially on the NPKNaMg plots as
					a result of increasing soil acidity. By 1948 the produce was too
					small to weigh, and the four-course rotation ceased in 1951. Soil
					acidity was corrected and the plots were then used to evaluate the
					P and K reserves accumulated up to 1951. During this period the
					original six plots were halved and two levels of soil organic
					matter were established by growing leys on one half. Subsequently,
					the plots were further sub-divided to build up different amounts of
					P and K in the soil. Crop yields were then related to the reserves
					of P and K in the soil and the effect of adding fresh P and K. The
					experiment ended in 1990, only archived crop and soil samples are
					now available.
				</p>

				<p>
					Taken from the <i>Rothamsted Guide to the Classical Experiments</i>
					2006, page 39.
				</p>

