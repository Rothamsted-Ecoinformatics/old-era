<p><a href=" Hoos/hoos_open_access_soc" target="_blank">Hoosfield Soil Carbon</a></p>
<p><a href=" Hoos/hoos_open_access_soc" target="_blank"><img class="intext" src="home/tyOAHBsoc.jpg" title="Hoosfield Open Access SOC" width="250" height="170" align="left" hspace="5" vspace="5" /></a></p>
<div align="justify"><strong>Changes in soil organic carbon (SOC) content</strong> in selected   treatments of the Hoosfield experiment, where spring barley has been grown each   year since 1852. 
SOC (0-23cm) has remained almost constant in the unfertilized plot since   the 1880s, at the equilibrium level for this farming system on this soil type.   
Annual applications of 35 t ha<sup>-1</sup> FYM increased SOC rapidly at first, then more   slowly. This treatment now contains around three times as much SOC as the 
unfertilized plot. The third treatment, given FYM for just 20 years between 1852   and 1871, still contains more SOC than the unfertilized plot.
