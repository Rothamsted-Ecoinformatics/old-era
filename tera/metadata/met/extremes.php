<p><b>Last updated 30th January 2020. </b></p>
<p>Rothamsted Research at Harpenden has the longest run of freely-available daily weather data in England  and has been sending returns to the UK Meteorological Office since at least  1878. In 2017, Rothamsted was recognised by the World Meteorological  Organization (WMO) as a <a href="https://public.wmo.int/en/our-mandate/what-we-do/observations/centennial-observing-stations" target="_blank">Centennial Observation Station</a> being one of the relatively small number  of sites in the UK that has been recording reliable observations continuously  for more than 100 years. Rainfall has been measured daily for more than 160  years, since 1853, using a 5-inch gauge and a much larger, and unusual, 1,000th  of an acre gauge. Subsequently, other weather variables were added to the list  including temperature from about 1878, sunshine hours from 1890, and wind  force/speed from 1915. Weather variables were recorded manually until the end  of 2003; an automatic data logger was installed to record the data  electronically from 1st January 2004*.</p>
<p>The Rothamsted weather records reflect  many of the memorable national weather events such as the heatwaves of 1976 and  2018, the severe winters of 1947 and 1963, the heavy rainfall of December 2015,  and the drought of 1921. The highs and lows include: 
