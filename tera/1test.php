					<H2>Exhaustion land, Rothamsted</H2>

					<div align="center">
						<img src="metadata/others/Hoosfield_625.jpg"
							title="Exhaustion Land, Hoosfield" width="625" height="176"
							alt="Exhaustion Land, Hoosfield" />

						<p>
							Unlike some of the other Classical experiments, which have been
							modified without losing the continuity of many of their
							treatments, the <b>Exhaustion Land</b> experiment has had several
							distinct phases since it started in 1856. From 1856 to 1901,
							annual dressings of N, P, K or FYM (from 1876 only) were applied.
							Wheat was grown initially (1856-1875) then potatoes (1876-1901).
							There were 10 plots from 1876 to 1901.
						</p>

						<p>From 1902 to 1939 no fertilizers or manures were applied and,
							with a few exceptions, cereals (usually spring barley) were
							grown. From 1940 to 1985, spring barley was grown and N
							fertilizer applied to all plots every year, initially at a single
							rate, but in 1976 the 10 main plots were divided to test four
							rates of N.</p>

						<p>
							In 1986, after a long period when the P residues in particular
							were being “exhausted”, it was decided to see how quickly this
							decline in soil fertility could be reversed. Annual, cumulative
							dressings of 0 v 44 v 87 v 131 kg P ha<sup>-1</sup>, as triple
							superphosphate, were tested on five of the original plots (each
							divided into four sub-plots). Basal N and K were applied such
							that these nutrients did not limit yield. Responses to fresh P
							were rapid. Applications of P stopped after seven years. No P was
							applied between 1993 and 1999, but since 2000, maintenance
							dressings, equivalent to offtakes by the crop, have been applied
							(not to the no-fresh-P sub-plots). Wheat has been grown since
							1992. Typically, it showed the same response to available-P as
							spring barley i.e. above a critical level, <i>on this soil</i>,
							of about 12 mg kg<sup>-1</sup> there is no further increase in
							yield.
						</p>

						<p>On the other half of the experiment, the effects of K residues
							(in the presence of basal P and N) on yield are investigated.
							Soils are sampled regularly to follow changes in plant-available
							P and K.</p>

						<p>
							Taken from the <i>Rothamsted Guide to the Classical Experiments</i>
							2006, pages 35-37.
