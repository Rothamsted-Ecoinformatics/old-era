<h3>e-RA History</h3>
<p>
e-RA was initially developed by Jackie Potts, Andy Caiger, Kevin Crowley, Sharon Brown, Paul Verrier, Christine Thomas, Roger Payne and 
Joe Perry, with funding from the Lawes Agricultural Trust and the Leverhulme Trust. Alan Todd and Gill Tuck subsequently prepared and 
curated the data, with Bill Moore responsible for the software and database application development. </p>

