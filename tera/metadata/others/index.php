<H2>Woburn Green Manuring Rotation experiment</H2>
<p>Rotation experiment at Stackyard Field, Series A, Woburn, 1936-1963. The predessesor of the Woburn Organic Manuring experiment.  </p>
<p><b>Design:</b> The experiment had two phases, 1936-1953 ('Old Scheme) and 1954-1963 ('New Scheme'). For full details see Rothamsted (1966) below. 
The Old Scheme measured the effects of four green manures (reygrass, red clover, mustard and tares) undersown or sown in spring and then ploughed in on two test crops, kale (then cabbages) and barley. 
There was also a control (no green manure, spring fallow). Tares were replaced with lupins and mustard by rape in 1946. The yields of the green manures were estimated. Yields of the test crops were taken. 
For a summary of results see Mann (1959) below.  </p>
<p>The New Scheme was a design of two blocks each of 40 plots. Each of two test crops, early potatoes and barley, were grown in rotation every year, following four green manuring treatments: 
trefoil and ryegrass, undersown in barley, and trefoil and ryegrass sown as a green manure after lifting early potatoes and tested on barley. For a summary of the results see Barnes & Clarke (1963) below.  </p>
