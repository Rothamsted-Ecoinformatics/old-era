<p><a href="<?php echo $metadata ; ?>Broadbalkplan1852-1925.pdf" target="_blank">Broadbalk plan 1852-1925 (pdf)</A> </p>

<p>Yields in the original Fisher paper were expressed as bushels/acre. A bushel is a measure of volume, eight gallons or 36.369 litres.
Grain yields in e-RA have since been converted to t/ha, using bushel weights, ie the weight of one bushel of wheat grain. Bushel weights were determined after harvest by weighing a bushel measure filled with grain.
See <a href="https://doi.org/10.1017/S0021859608007958" target="_blank"> Atkinson et al, 2008 </a>  for further details.  </p>





