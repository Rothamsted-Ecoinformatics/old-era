							<A NAME="SEC6"></a>
							<H2>Fosters Ley-Arable, Rothamsted</H2>

							<div align="center">
								<img src="metadata/others/Fosters_Ley_Arable_675.jpg"
									title="Fosters Ley Arable" width="675" height="217"
									alt="Fosters Ley Arable" />

								<p>
									<b>Design:</b> Four blocks of five plots (total of 20 plots)
									each testing the effects of permanent grass, rotational grass,
									arable and bare fallow, were established on a long-term arable
									site (>100 years). Two blocks are in winter wheat test crops
									each year whilst the other two are in treatment crops. Yields
									are no longer taken, but soils are taken about every 5 years.
								</p>

								<p>
									<b>Strengths:</b> The experiment provides comparison with the
									Highfield Ley-arable experiment where similar treatments were
									established on a long-term grassland site and the Woburn
									Ley-arable experiment. Many archived samples and data are
									available.
								</p>

								<p>
									<b>Background:</b> The Rothamsted <b>Highfield and Fosters
										Ley-Arable experiments </b>started in 1949. The two sites have
									the same soil type but the cropping histories of each are very
									different. Highfield had been in permanent grass since 1838; on
									this site some plots stayed in permanent grass, others went
									into continuous arable cropping and some alternated between
									leys and arable. In contrast, Fosters has been in arable
									cropping for several centuries; on this site some plots stayed
									in continuous arable, some went into permanent grass and others
									alternated between leys and arable. Although yields are no
									longer measured, SOM continues to be monitored. Even after more
									than 60 years, SOM contents on these experiments have still not
									reached new equilibria. In soils ploughed out of permanent
									grass, SOM is still declining whilst it continues to increase
									in soils under permanent grass.
								</p>

