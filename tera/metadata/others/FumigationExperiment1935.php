<p><strong>Conclusions</strong>
<p>Carbon disulphide jelly and seekay produced significant decreases in the number of cysts, the falling-off in the decrease at the higher level of dressing being small for carbon disulphide jelly and not quite significant for seekay. The decreases due to chlordinitrobenzene and cymag were not significant and were signifiantly less than those due to the other two fumigants. 
