<p>Human nutrition</p>
<table width="631" border="2">
  <tr>
    <td width="262" height="200">&nbsp;</td>
    <td width="351">Nutritional  quality of wheat grain: There is evidence from Broadbalk that  an unintended consequence of the global change to short-strawed wheat varieties  has been a decreased concentration of minerals in grain, with potential impacts  on human nutrition and health. Analysis of archived grain showed that  concentrations of Zn, Fe, Cu and Mg decreased following the change to  short-straw varieties. Concentrations of these minerals in the soil either  increased or remained constant during the duration of the experiment. (Fan et al, 2008). 
