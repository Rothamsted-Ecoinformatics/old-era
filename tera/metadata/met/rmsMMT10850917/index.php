<h1>Description</h1>
<p>This  dataset represents  the mean monthly temperature at Rothamsted  from October 1985 - September 2017  and is based on continuous daily records measured at the site.</p>
<p><strong>Publication</strong></p>
<p>This dataset was compiled for the publication J. Macholdt, H.-P. Piepho, B.  Honermeier, S. Perryman, A. Macdonald and P.  Poulton. The effects of cropping sequence, fertilisation and straw management on the yield stability of winter wheat (1986-2017) in the Broadbalk Wheat Experiment, Rothamsted, UK. (2020). Journal of Agricultural Science.</p>
<p>This publication also includes reference to the accompanying dataset <a href="http://www.era.rothamsted.ac.uk/Met/rmsMMR10850917" target="_blank">Mean monthly rainfall at Rothamsted October 1985 - September 2017</a>.</p>
<p><strong>MEASUREMENTS</strong></p>
<p>The mean daily temperature is measured over a 24 hour period 0900 to 0900 GMT and calculated from the daily maximum (Tmax) and minimum temperatures (Tmin); the mean daily temperature = (Tmax = Tmin)/2. The temperature is recorded at 1.5 m height above the ground surface within a naturally  aspirating temperature screen (Stevenson screen). Temperature was first recorded in 1878, Tmax was then measured using a mercury column thermometer and Tmin using a spirit-in-glass with indicator bar minimum thermometer. Data were collected daily manually until 2004  using a standard protocol<strong>.</strong> Since 2004, temperatures have been recorded automatically by Automatic Weather Station thermistors (electronic temperature probes, Campbell Scientific, Type 107). See <a href="http://www.era.rothamsted.ac.uk/Met/instrumentdescription" target="_blank">Temperature</a> for further information.</p>
<p><strong>LOCATION</strong></p>
<p>Rothamsted Meteorological Station, Harpenden, Hertfordshire, England 51.82 N 0.37 W 128 m asl. </p>
<h1>Provenance &amp; Quality</h1>
<p>The monthly summary data contained in this spreadsheet are derived from  daily data measured at Rothamsted Meteorological Station, Harpenden. Total monthly data is determined from daily data using  Genstat 19th Edition. Verification includes checks for instrument errors, for missing data and outliers. Missing values are acquired form the UK Met Office Station on site. The original raw daily data is available, after registering, from the e-RA database. Please contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a> for an access password and further details.</p>
