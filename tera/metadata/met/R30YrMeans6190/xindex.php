<h1>Description</h1>
<p>This dataset consists of  the 30 years monthly mean meteorological data measured at Rothamsted Research, Harpenden, UK, from January 1961 until December 1990,
and is derived from daily measurements taken at the site. Thirty years is chosen as a preiod long enough to eliminate year-to-year variations.</p>
<p><strong>MEASUREMENTS</strong></p>
<li><b>Rain: Mean total monthly rainfall: </b>including all precipitation (snow, rain, mist and fog) captured in a 5 inch rain gauge (mm per month); <br />
<li><b>Rain: Mean total monthly rainfall: </b>including all precipitation (snow, rain, mist and fog) captured in a 1/1000 acre rain gauge (mm per month); <br />
</li>
<li><strong>Sunshine hours: Mean sunshine hours: </strong>the mean hours of sunshine (hrs)<br />
</li>
<li><b>Maximum temperature: Mean maximum monthly air temperature:</b> the mean of the maximum and minimum air temperature (degrees C);</li>
<li><b>Minimum temperature: Mean minimum monthly air temperature:</b> the mean of the maximum and minimum air temperature (degrees C);</li>
<li><b>Mean temperature: mean monthly air temperature:</b> the mean of the maximum and minimum air temperature (degrees C);</li>
<li><b>Soil temperature under grass 30cm:Mean monthly soil temperature:</b> the soil temperature in ground under bare soil at 30cm depth, measured at 0900 GMT (degrees C); </li>
<li><b>Soil temperature under grass 30cm: Mean monthly soil temperature:</b> the soil temperature in ground under bare soil at 30cm depth, measured at 0900 GMT (degrees C); </li>
<li><b>Wind: Mean monthly wind speed:</b> measured at 2 metres above ground level, average over 24 hours (km per hour); </li>
<li><b>Evaporation: Total monthly evaporation over open water:</b> mean of two open water tanks installed in the spring of each year (mm per month). </li>
<li><b>Radiation: Mean daily solar radiation:</b> measured with a Kipp Solarimeter (MJ m<sup>-2</sup> day<sup>-1</sup> ); </li>
