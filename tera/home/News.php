<p> <a href="http://www.era.rothamsted.ac.uk/Met/importantmeteorologicaldataupdates" target="out"> <b>13-08-2018 Correction to ROTHMET RAIN measurements before 2004</b></a>: Since April 2004 RAIN at Rothamsted (ROTHMET) has been measured with an ARG100 automatic tipping bucket. This has been found to capture 10% more rainfall than the manual 5 inch cyclindrical guage used previously. A double mass curve analysis of annual data from 1990-2017 gave an overall correction factor of 10%.
We recommend that when data is downloaded that spans both gauges, the 5 inch gauge data is multiplied by 1.1, to make it comparable with data from the ARG100.   This correction only applies to monthly or annual totals, and to the variable RAIN at Rothamsted. Please contact the e-RA Curators for more details.</p>
