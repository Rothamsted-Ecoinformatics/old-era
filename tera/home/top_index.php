<h1>Welcome to e-RA, the <em>electronic</em> Rothamsted Archive.</h1>

<p><b>Contact the <a href="mailto:era@rothamsted.ac.uk">e-RA Curators</a> to arrange a password for the database.</b></p>
<p>e-RA provides a permanent managed database for secure storage of data from Rothamsted's Long-term Experiments, the oldest, continuous agronomic experiments in the world. Together with the accompanying meteorological records, associated documentation and sample archive,
  it is a unique historical record of experiments that have been measured continuously for over 170 years.
  Users have access to experimental data and specialist background information on the effects of agricultural practices on crops, soils and associated ecosystems. </p>
