<h1>Case Studies</h1>

<p>Data held in e-RA provide a unique historic record from experiments that have measured the effects of 
agricultural practices on soils, crops and associated ecosystems continuously for over 160 years. Together
 with the retrospective analysis of archived material from the Rothamsted Sample Archive, these data
 continue to reveal new insights and important findings of relevance to today's agriculture and the wider
 environment. 

 </p>
 
