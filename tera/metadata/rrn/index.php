<h1>Rothamsted Ley Arable</h1>
<p><b><a href="RRN/rothla_open_access_soc">Open access data available:</a> </b>Freely available data on selected soil organic carbon changes over time 1948-2008 </p>

<p><b>Background:</b> The Rothamsted <b>Highfield and Fosters Ley-Arable 
	experiments </b>started in autumn 1948. The two sites have the same soil type
	but the cropping histories of each are very different. Highfield had
	been in permanent grass since 1838 (Lawes &amp; Gilbert, 1885); on this site some plots stayed in
	permanent grass, others went into continuous arable cropping and some
	alternated between leys and arable. In contrast, Fosters has been in
	arable cropping for several centuries; on this site some plots stayed
	in continuous arable, some went into permanent grass and others
	alternated between leys and arable. Yields are no longer
	measured, and were last recorded in <b>1990</b>. However, the SOM continues to be monitored every 5-10 years. After 40 years,
	SOM contents on these experiments had not reached new
	equilibria. In soils ploughed out of permanent grass, SOM was still
	declining whilst it continued to increase in soils under permanent
grass. However, after 60 years a new equilibrium was being reached.</p>
<p>Background taken from the <i><a href="home/Web_LTE_Guidebook_2018_2019-reprint.pdf" target="_blank">Rothamsted Guide to the Classical
Experiments</a></i><a href="home/Web_LTE_Guidebook_2018_2019-reprint.pdf"> 2018</a>, page 37.</p>
<h2>Highfield Ley-Arable, Rothamsted</h2>
<div align="center">
	<img src="<?=$metadata?>Highfield_Ley_Arable_2015_675.jpg"
		title="Highfield Ley Arable" width="675" height="252"
		alt="Highfield Ley Arable" />
</div>
<p>
	<b>R/RN/1</b> Effects of continuous arable and ley arable cropping systems on
			soil organic matter and fertility; originally long-term grassland
(&gt;100 years).</p>
<p><b>Design:</b> Four blocks of four plots (total of 16 plots, each 300m<sup>2</sup>)
	each testing the effects of permanent grass, rotational grass, arable
	and bare fallow, were established on a long-term grassland site (>100
	years). Two blocks are in winter wheat test crops each year whilst the
	other two are in treatment crops. Yields are no longer taken, but soils
	are taken about every 5 years. In 2008 two plots within each block were
	used to establish the Highfield Conversion Experiment (see below).
</p>

<p>
	<b>Strengths:</b> It is the only site at Rothamsted available for studying
	the effects of converting long-term grass to arable and bare-fallow
	treatments, on SOC and fertility. It provides contrasting comparisons with
	the Fosters Ley-arable experiment where similar treatments were
	established on a long-term arable site and the Woburn Ley-arable
	experiment. Many archived samples and data are available.
</p>

<P>
	For more details and references, see <b>Fosters Ley-Arable</b>, below.
</p>

