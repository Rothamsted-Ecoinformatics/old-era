<h1>Description </h1>
<p>The accumulation of organic carbon in soil and tree biomass has been measured on two contrasting sites at Rothamsted that were fenced off in the 1880s and left to revert naturally to woodland. 
  Broadbalk, on calcareous soil, and <a href="Wilderness/wilderness_open_access_geescroft_oc">Geescroft Wilderness</a>, on acidic soil. Previously, 
  both sites had been in arable cultivation for centuries. </p>
<p>The <b>Broadbalk</b> site was part of the Broadbalk wheat experiment, and had grown unmanured winter wheat since autumn 1843. Large amounts of chalk had been applied 
  to the soil in the late 18th - early 19th centuries, and the surface soil pH is still neutral (pH 7.7 in 1999). The site is small (0.2ha). It was previously a long-term arable site, 
  and was allowed to naturally regenerate to deciduous woodland following abandonment of arable cropping. The last wheat crop was sown in autumn 1881, but not harvested. In 1882, the 
  site was then fenced off, left uncultivated and unharvested. No fertilizer or manure were applied. In  1900 it was divided into two halves: one half remained untouched (regenerating woodland). The other half had all woody species removed annually (stubbed), to allow open ground vegetation to develop. In 1957 the stubbed section was divided into two, one half remains as 'stubbed', the other half was mown for three years, grazed by sheep  each year from 1960-2000, mown since 2001 (herbage not removed). Data for 'stubbed' and grassland areas is not shown, but is available on request from the e-RA curators.</p>
<p> Broadbalk Wilderness is now dominated by ash (<i>Fraxinus excelsior</i>), with sycamore (<i>Acer pseudoplatanus</i>) and hawthorn (<i>Craetagus monogyna</i>); 
  in contrast, the acidic Geescroft Wilderness  is now a deciduous wood dominated by oak (<i>Quercus robur</i>). </p>
<p>The figure  shows the accumulation of organic carbon in the soil and in tree root and above-ground biomass in the Broadbalk Wilderness, 1881-1999. The soil has been sampled to an equivalent depth of 69cm (see Poulton <i>et al</i>, 2003 for further details). The site has gained 3.39 t C/ha/year over the 120 year period (0.54 t in the soil plus an estimated 2.85 t in trees and roots). Unlike the Geescroft wilderness site, the amount of C in the litter layer is negligible, as the litter decomposes each year. Data on the accumulation of nitrogen is also available from the <a href="mailto:era@rothamsted.ac.uk" target="_blank">e-RA curators.</a></p>
<p><strong>SOIL</strong></p>
<p>Silty clay loam surface overlying clay-with-flints, over chalk at a depth of several metres.<br />
  Topsoil (0-23cm) contains 20-25% clay, rising to 50-60% at depths of 1 metre.<br />
  Stagnogleyic palaeo-argillic brown earth (Soil Survey of England and Wales classification).<br />
  Aquic Paleudalf (USDA classification). <br />
  Chromic Luvisol (FAO classification). <br />
  Soil pH is near neutral (7.7 in 1999), as Broadbalk received large applications of chalk in the late 18th or early 19th centuries. <br />
  Soil comparisons are made on an equivalent depth basis, to allow for changes in soil bulk density, see Poulton et al., 2003 for further details.</p>
